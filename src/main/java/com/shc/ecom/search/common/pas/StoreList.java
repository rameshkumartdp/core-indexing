package com.shc.ecom.search.common.pas;

/**
 * @author pchauha
 */
public class StoreList {
    private String storeId;
    private String available;
    private String type;
    private String isDisplayAvailable;

    public String getStoreId() {
        return storeId;
    }

    public void setStoreId(String storeId) {
        this.storeId = storeId;
    }

    public String getAvailable() {
        return available;
    }

    public void setAvailable(String available) {
        this.available = available;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getIsDisplayAvailable() {
        return isDisplayAvailable;
    }

    public void setIsDisplayAvailable(String isDisplayAvailable) {
        this.isDisplayAvailable = isDisplayAvailable;
    }

}
